package leetcode;

public class LinkedListCycle {
    public SingleListNode detectCycle(SingleListNode head) {
        SingleListNode slow = head;
        SingleListNode fast = head;
        while(fast != null && fast.next != null){
            slow = slow.next;
            fast = fast.next.next;
            if(slow == fast){
                slow = head;
                while(slow != fast){
                    slow = slow.next;
                    fast = fast.next;
                }
                return slow;
            }
        }
        return null;
    }
}
